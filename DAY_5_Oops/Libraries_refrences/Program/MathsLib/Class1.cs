namespace MathsLib{

public class Alzebra
{
    /// <summary>
    /// Adds two integers.
    /// </summary>
    /// <param name="a"></param>
    /// <param name="b"></param>
    /// <returns></returns>
    public int Add(int a, int b)
    {
        return a + b;
    }

    /// <summary>
    /// Subtracts second integer from first.
    /// </summary>
    /// <param name="a"></param>
    /// <param name="b"></param>
    /// <returns></returns>
    public int Subtract(int a, int b)
    {
        return a - b;
    }
}
}